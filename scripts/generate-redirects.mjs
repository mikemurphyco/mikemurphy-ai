import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const articleRoot = 'src/content/articles';
const redirectFile = 'public/_redirects';

function walk(dir, files = []) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const file = join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(file, files);
    } else if (/\.(md|mdx)$/.test(entry.name)) {
      files.push(file);
    }
  }

  return files;
}

function frontmatterValue(source, key) {
  const match = source.match(new RegExp(`^${key}:\\s*"?([^"\\n]+)"?\\s*$`, 'm'));
  return match?.[1]?.trim();
}

function frontmatterList(source, key) {
  if (source.match(new RegExp(`^${key}:\\s*\\[\\]\\s*$`, 'm'))) return [];

  const start = source.search(new RegExp(`^${key}:\\s*$`, 'm'));
  if (start < 0) return [];

  const lines = source.slice(start).split('\n').slice(1);
  const values = [];

  for (const line of lines) {
    if (/^\S/.test(line)) break;
    const item = line.match(/^\s*-\s*"?([^"\n]+)"?/);
    if (item) values.push(item[1].trim());
  }

  return values;
}

function normalizePath(value) {
  if (!value) return null;

  try {
    return new URL(value).pathname;
  } catch {
    return value.startsWith('/') ? value : `/${value}`;
  }
}

function canonicalDestination(article) {
  const categories = article.categories.map((category) => category.toLowerCase());

  if (categories.includes('episodes') || /^ep\d+$/i.test(article.slug)) {
    return `/podcast/${article.slug}/`;
  }

  if (categories.includes('tutorials')) {
    return `/tutorials/${article.slug}/`;
  }

  return `/articles/${article.slug}/`;
}

const articles = walk(articleRoot)
  .map((file) => {
    const source = readFileSync(file, 'utf8');
    return {
      file,
      slug: frontmatterValue(source, 'slug'),
      permalink: frontmatterValue(source, 'permalink'),
      legacyPermalink: frontmatterValue(source, 'legacyPermalink'),
      visibility: frontmatterValue(source, 'visibility'),
      categories: frontmatterList(source, 'categories'),
    };
  })
  .filter((article) => article.slug && article.visibility !== 'hidden' && article.visibility !== 'draft');

const redirects = new Map([
  ['/aiunplugged/', '/ai-unplugged/'],
  ['/ai-unplugged/issues/1/', '/ai-unplugged/issues/001/'],
]);

for (const article of articles) {
  const destination = canonicalDestination(article);
  const sources = [
    normalizePath(article.permalink),
    normalizePath(article.legacyPermalink),
    `/${article.slug}/`,
    `/articles/${article.slug}/`,
  ].filter(Boolean);

  for (const source of sources) {
    if (source !== destination && !redirects.has(source)) {
      redirects.set(source, destination);
    }
  }
}

const lines = [
  '# Generated by scripts/generate-redirects.mjs',
  '# Old mikemurphy.co-style root slugs -> Astro canonical routes.',
  ...[...redirects.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([source, destination]) => `${source} ${destination} 301`),
  '',
];

writeFileSync(redirectFile, lines.join('\n'));

console.log(`Wrote ${redirects.size} redirects to ${redirectFile}`);
